public class Ejercicio1 {
    public static void main(String[] args) throws Exception {
        int x=144;
        int y=999;
        System.out.println("Los numeros que se van a usar son: " +x +" y " +y);
        System.out.println("La suma de " +y +" y " +x +" es " +(y+x));
        System.out.println("La resta de " +y +" y " +x +" es " +(y-x));
        System.out.println("La división de " +y +" entre " +x +" es " +((float)y/(float)x));
        System.out.println("La multiplicación de " +y +" entre " +x +" es " +(y*x));
    }
}
